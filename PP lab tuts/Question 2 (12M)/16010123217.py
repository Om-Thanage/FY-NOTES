num = list(input("Enter your integer\n"))
def evenoddchecker(num):
    even = 0
    odd = 0
    for i in range(0, len(num)):
        if int(num[i])%2==0:
            even +=1
        else:
            odd +=1
    print("The total number of even numbers in that digit is", even)
    print("The total number of odd numbers in that digit is", odd)
evenoddchecker(num)












































































































































































































































































































































































